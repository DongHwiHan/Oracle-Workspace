FROM TB_STUDENT
WHERE TO_CHAR(ENTRANCE_DATE,'YYYY') - TO_CHAR(TO_DATE(SUBSTR(STUDENT_SSN,1,6),'RRMMDD'),'RRRR') > 19 ;

--6.
