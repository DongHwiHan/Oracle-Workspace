SELECT STUDENT_NO AS "학번",STUDENT_NAME AS "이름",TO_CHAR(ENTRANCE_DATE, 'YYYY-MM-DD') AS 입학년도
FROM TB_STUDENT
WHERE DEPARTMENT_NO = 002 ORDER BY ENTRANCE_DATE; 

--2.
SELECT PROFESSOR_NAME, PROFESSOR_SSN
FROM TB_PROFESSOR
WHERE LENGTH(PROFESSOR_NAME) !=3 ORDER BY PROFESSOR_NAME;

--3.
SELECT PROFESSOR_NAME AS "교수이름", TO_CHAR(SYSDATE,'YYYY')-(SUBSTR(PROFESSOR_SSN,1,2) +1900)  AS "나이" 
FROM TB_PROFESSOR
WHERE SUBSTR(PROFESSOR_SSN, 8,1)= 1 ORDER BY PROFESSOR_SSN DESC;

--4.
SELECT SUBSTR(PROFESSOR_NAME, 2, 2) AS "이름"
FROM TB_PROFESSOR;

--5.
